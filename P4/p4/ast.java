abstract class DeclNode extends ASTnode {
    abstract public void nameAnalyze(SymTable symTable);
}

class VarDeclNode extends DeclNode {
    public VarDeclNode(TypeNode type, IdNode id, int size) {
        myType = type;
        myId = id;
        mySize = size;
    }

    public void unparse(PrintWriter p, int indent) {
        addIndentation(p, indent);
        myType.unparse(p, 0);
        p.print(" ");
        myId.unparse(p, 0);
        p.println(";");
    }

    public void nameAnalyze(SymTable symTable) {
        String type = myType.getType();
        // check if invalid type
        if (type.equals("void")) {
            ErrMsg.fatal(
                myId.getMyLineNum(),
                myId.getMyCharNum(),
                "Non-function declared void");
            return;  // stop processing
        }
        Sym symbol = new Sym(type);
        try {
            symTable.addDecl(myId.getMyStrVal(), symbol);
        } catch (DuplicateSymException e) {
            // multiple declarations! Log error.
            ErrMsg.fatal(
                myId.getMyLineNum(),
                myId.getMyCharNum(),
                "Multiply declared identifier"
            );
        } catch (EmptySymTableException e) {
            // do nothing since this means there was clearly no duplicate
            System.err.println(e.toString());
        }
    }

    private TypeNode myType;
    private IdNode myId;
    private int mySize;  // use value NOT_STRUCT if this is not a struct type

    public static int NOT_STRUCT = -1;
}

class FnDeclNode extends DeclNode {
    public FnDeclNode(TypeNode type,
                      IdNode id,
                      FormalsListNode formalList,
                      FnBodyNode body) {
        myType = type;
        myId = id;
        myFormalsList = formalList;
        myBody = body;
    }

    public void unparse(PrintWriter p, int indent) {
        addIndentation(p, indent);
        myType.unparse(p, 0);
        p.print(" ");
        myId.unparse(p, 0);
        p.print("(");
        myFormalsList.unparse(p, 0);
        p.println(") {");
        myBody.unparse(p, indent+4);
        p.println("}\n");
    }

    public void nameAnalyze(SymTable symTable) {
        String type = myType.getType();
        // ensure function name is not in use with same params
        Sym sym = new FnSym(type, null);  // TODO change from null
        try {
            symTable.addDecl(myId.getMyStrVal(), sym);
        } catch (DuplicateSymException e) {
            ErrMsg.fatal(myId.getMyLineNum(), myId.getMyCharNum(), "Multiply declared identifier");
        } catch (EmptySymTableException e) {}
        // Then we add a new scope with params declared in it, even if an
        // error occurred.
        symTable.addScope();
        myFormalsList.nameAnalyze(symTable);
        myBody.nameAnalyze(symTable);
        try {
            symTable.removeScope();
        } catch (EmptySymTableException e) {
            // should still have the scope from before, the function's scope
            System.err.println(e.toString());
        }
    }

    private TypeNode myType;
    private IdNode myId;
    private FormalsListNode myFormalsList;
    private FnBodyNode myBody;
}

class FormalDeclNode extends DeclNode {
    public FormalDeclNode(TypeNode type, IdNode id) {
        myType = type;
        myId = id;
    }

    public void unparse(PrintWriter p, int indent) {
        myType.unparse(p, 0);
        p.print(" ");
        myId.unparse(p, 0);
    }

    public void nameAnalyze(SymTable symTable) {
        String name = myId.getMyStrVal();
        String type = myType.getType();
        if (type.equals("void")) {
            ErrMsg.fatal(
                myId.getMyLineNum(),
                myId.getMyCharNum(),
                "Non-function declared void");
            return;  // stop processing
        }
        Sym sym = new Sym(type);
        try {
            symTable.addDecl(name, sym);
        } catch (DuplicateSymException e) {
            // another formal already has this name, do not add it to table
            ErrMsg.fatal(myId.getMyLineNum(), myId.getMyCharNum(), "Multiply declared identifier");
        } catch (EmptySymTableException e) {}
    }

    private TypeNode myType;
    private IdNode myId;
}

